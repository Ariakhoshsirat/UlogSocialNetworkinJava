package FB;

public class Interest {
		private String name;
		private Integer interestId;
		
		
		
		
		public void setName(String name) {
			this.name = name;
		}
		public String getName() {
			return name;
		}
		public void setInterestId(Integer interestId) {
			this.interestId = interestId;
		}
		public Integer getInterestId() {
			return interestId;
		}
}
