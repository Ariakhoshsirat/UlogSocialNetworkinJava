package FB;


public class Friend {
	
	private Integer number;
	private Integer friendId;
	
	
	public void setFriendId(Integer friendID) {
		this.friendId = friendID;
	}
	
	public Integer getFriendId() {
		return friendId;
	}
	
	public void setNumber(Integer number) {
		this.number = number;
	}
	
	public Integer getNumber() {
		return number;
	}
	
}
	